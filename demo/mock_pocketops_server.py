def store_submitted_work_order(payload: Dict[str, object]) -> Dict[str, object]:
    work_order_id = str(payload.get("workOrderId") or "")
    client_submission_id = str(payload.get("clientSubmissionId") or "")
    if not work_order_id:
        raise ValueError("missing workOrderId")
    if not client_submission_id:
        raise ValueError("missing clientSubmissionId")

    server_id = f"server-{work_order_id}"
    submitted_at = datetime.now(timezone.utc).isoformat()
    SUBMITTED_WORK_ORDERS[client_submission_id] = {
        "serverWorkOrderId": server_id,
        "submittedAt": submitted_at,
        "payload": payload,
    }
    return {
        "accepted": True,
        "serverWorkOrderId": server_id,
        "clientSubmissionId": client_submission_id,
        "submittedAt": submitted_at,
        "queuedCount": len(SUBMITTED_WORK_ORDERS),
    }


def authenticate_demo_user(
    username: str,
    password: str,
    expected_username: str,
    expected_password: str,
) -> bool:
    return hmac.compare_digest(username, expected_username) and hmac.compare_digest(password, expected_password)


def create_auth_session(username: str) -> Dict[str, object]:
    token = secrets.token_urlsafe(32)
    expires_at = datetime.now(timezone.utc) + timedelta(seconds=DEFAULT_TOKEN_TTL_SECONDS)
    session = {
        "accessToken": token,
        "userId": username,
        "displayName": "维修工程师",
        "role": "maintenance_engineer",
        "expiresInSeconds": DEFAULT_TOKEN_TTL_SECONDS,
        "expiresAt": expires_at,
    }
    AUTH_SESSIONS[token] = session
    return session


def public_auth_session(session: Dict[str, object]) -> Dict[str, object]:
    return {
        "accessToken": session["accessToken"],
        "userId": session["userId"],
        "displayName": session["displayName"],
        "role": session["role"],
        "expiresInSeconds": session["expiresInSeconds"],
    }


def parse_json_body(handler: BaseHTTPRequestHandler) -> Dict[str, object]:
    length = int(handler.headers.get("Content-Length", "0"))
    raw = handler.rfile.read(length) if length > 0 else b"{}"
    if not raw:
        return {}
    return json.loads(raw.decode("utf-8"))


def parse_range_header(range_header: str | None, size: int) -> Tuple[int, int] | None:
    if not range_header or not range_header.startswith("bytes="):
        return None
    first_part = range_header[len("bytes="):].split(",", 1)[0].strip()
    if "-" not in first_part:
        raise ValueError("invalid byte range")
    start_text, end_text = first_part.split("-", 1)
    if start_text == "":
        if not end_text:
            raise ValueError("invalid suffix range")
        suffix_length = int(end_text)
        if suffix_length <= 0:
            raise ValueError("invalid suffix range")
        start = max(0, size - suffix_length)
        end = size - 1
        return (start, end)

    start = int(start_text)
    end = size - 1 if end_text == "" else int(end_text)
    if start < 0 or start >= size or end < start:
        raise ValueError("range out of bounds")
    return (start, min(end, size - 1))


class PocketOpsDemoHandler(BaseHTTPRequestHandler):
    server_version = "PocketOpsMock/1.1"

    def do_GET(self) -> None:  # noqa: N802
        self.handle_request(with_body=True)

    def do_HEAD(self) -> None:  # noqa: N802
        self.handle_request(with_body=False)

    def do_POST(self) -> None:  # noqa: N802
        parsed = urlparse(self.path)

        if parsed.path == "/api/pocketops/auth/login":
            self.handle_login()
            return

        if parsed.path == "/api/pocketops/materials/query":
            if not self.require_auth():
                return

            try:
                payload = parse_json_body(self)
            except json.JSONDecodeError as exc:
                self.send_json({"error": f"invalid_json: {exc}"}, status=HTTPStatus.BAD_REQUEST)
                return

            self.send_json(build_materials(self.base_url(), payload))
            return

        if parsed.path == "/api/pocketops/work-orders/submit":
            if not self.require_auth():
                return

            try:
                payload = parse_json_body(self)
                result = store_submitted_work_order(payload)
            except json.JSONDecodeError as exc:
                self.send_json({"error": f"invalid_json: {exc}"}, status=HTTPStatus.BAD_REQUEST)
                return
            except ValueError as exc:
                self.send_json({"error": str(exc)}, status=HTTPStatus.BAD_REQUEST)
                return

            self.send_json(result, status=HTTPStatus.CREATED)
            return

        self.send_json({"error": "not_found"}, status=HTTPStatus.NOT_FOUND)

    def handle_login(self) -> None:
        try:
            payload = parse_json_body(self)
        except json.JSONDecodeError as exc:
            self.send_json({"error": f"invalid_json: {exc}"}, status=HTTPStatus.BAD_REQUEST)
            return

        username = str(payload.get("username") or "")
        password = str(payload.get("password") or "")
        if not authenticate_demo_user(
            username=username,
            password=password,
            expected_username=getattr(self.server, "demo_username", DEFAULT_DEMO_USERNAME),
            expected_password=getattr(self.server, "demo_password", DEFAULT_DEMO_PASSWORD),
        ):
            self.send_json({"error": "invalid_credentials"}, status=HTTPStatus.UNAUTHORIZED)
            return

        self.send_json(public_auth_session(create_auth_session(username)))

    def handle_request(self, with_body: bool) -> None:
        parsed = urlparse(self.path)
        if parsed.path == "/api/pocketops/bootstrap/manifest":
            if not self.require_auth(with_body=with_body):
                return
            self.send_json(
                build_manifest(
                    self.base_url(),
                    sync_version=getattr(self.server, "sync_version", DEFAULT_SYNC_VERSION),
                    min_supported_app_version=getattr(
                        self.server,
                        "min_supported_app_version",
                        DEFAULT_MIN_SUPPORTED_APP_VERSION,
                    ),
                ),
                with_body=with_body,
            )
            return

        asset = ASSETS.get(parsed.path)
        if asset is None:
            self.send_json({"error": "not_found"}, status=HTTPStatus.NOT_FOUND, with_body=with_body)
            return

        if not self.require_auth(with_body=with_body):
            return

        self.send_asset(asset, with_body=with_body)

    def require_auth(self, with_body: bool = True) -> bool:
        header = self.headers.get("Authorization", "")
        if not header.startswith("Bearer "):
            self.send_json({"error": "missing_bearer_token"}, status=HTTPStatus.UNAUTHORIZED, with_body=with_body)
            return False

        token = header.removeprefix("Bearer ").strip()
        session = AUTH_SESSIONS.get(token)
        if session is None:
            self.send_json({"error": "invalid_bearer_token"}, status=HTTPStatus.UNAUTHORIZED, with_body=with_body)
            return False

        expires_at = session.get("expiresAt")
        if not isinstance(expires_at, datetime) or expires_at <= datetime.now(timezone.utc):
            AUTH_SESSIONS.pop(token, None)
            self.send_json({"error": "expired_bearer_token"}, status=HTTPStatus.UNAUTHORIZED, with_body=with_body)
            return False

        return True

    def base_url(self) -> str:
        host = self.headers.get("Host")
        if host:
            return f"http://{host}"
        server_host, server_port = self.server.server_address[:2]
        return f"http://{server_host}:{server_port}"

    def send_json(
        self,
        payload: Dict[str, object],
        status: HTTPStatus = HTTPStatus.OK,
        with_body: bool = True,
    ) -> None:
        content = json.dumps(payload, ensure_ascii=False, indent=2).encode("utf-8")
        self.send_response(status)
        self.send_header("Content-Type", "application/json; charset=utf-8")
        self.send_header("Content-Length", str(len(content)))
        self.send_header("Cache-Control", "no-store")
        self.end_headers()
        if with_body:
            self.wfile.write(content)

    def send_asset(self, asset: FileAsset, with_body: bool) -> None:
        range_header = self.headers.get("Range")
        status = HTTPStatus.OK
        content = asset.content
        start = 0
        end = len(content) - 1
        try:
            parsed_range = parse_range_header(range_header, len(content))
        except ValueError:
            self.send_response(HTTPStatus.REQUESTED_RANGE_NOT_SATISFIABLE)
            self.send_header("Content-Range", f"bytes */{len(content)}")
            self.end_headers()
            return

        if parsed_range is not None:
            start, end = parsed_range
            content = asset.content[start : end + 1]
            status = HTTPStatus.PARTIAL_CONTENT

        self.send_response(status)
        self.send_header("Content-Type", asset.mime_type)
        self.send_header("Content-Length", str(len(content)))
        self.send_header("Accept-Ranges", "bytes")
        self.send_header("ETag", asset.sha256)
        self.send_header("Cache-Control", "no-store")
        if status == HTTPStatus.PARTIAL_CONTENT:
            self.send_header("Content-Range", f"bytes {start}-{end}/{len(asset.content)}")
        self.end_headers()
        if with_body:
            self.wfile.write(content)

    def log_message(self, fmt: str, *args: object) -> None:
        sys.stdout.write("%s - - [%s] %s\n" % (self.address_string(), self.log_date_time_string(), fmt % args))


def iter_candidate_urls(host: str, port: int) -> Iterable[str]:
    yield f"http://127.0.0.1:{port}"
    if host not in {"127.0.0.1", "localhost", "0.0.0.0", "::"}:
        yield f"http://{host}:{port}"
    try:
        lan_ip = socket.gethostbyname(socket.gethostname())
    except OSError:
        return
    if lan_ip and lan_ip not in {"127.0.0.1", host}:
        yield f"http://{lan_ip}:{port}"


def main() -> None:
    parser = argparse.ArgumentParser(description="Run the PocketOps demo mock server.")
    parser.add_argument("--host", default="0.0.0.0", help="Bind host, default: 0.0.0.0")
    parser.add_argument("--port", type=int, default=8080, help="Bind port, default: 8080")
    parser.add_argument(
        "--knowledge-graph",
        type=Path,
        default=None,
        help="Override Android assets knowledge_graph.json path.",
    )
    parser.add_argument(
        "--sync-version",
        default=DEFAULT_SYNC_VERSION,
        help=f"Manifest syncVersion, default: {DEFAULT_SYNC_VERSION}",
    )
    parser.add_argument(
        "--min-supported-app-version",
        default=DEFAULT_MIN_SUPPORTED_APP_VERSION,
        help=f"Manifest minSupportedAppVersion, default: {DEFAULT_MIN_SUPPORTED_APP_VERSION}",
    )
    parser.add_argument(
        "--demo-username",
        default=DEFAULT_DEMO_USERNAME,
        help=f"Demo login username, default: {DEFAULT_DEMO_USERNAME}",
    )
    parser.add_argument(
        "--demo-password",
        default=DEFAULT_DEMO_PASSWORD,
        help="Demo login password.",
    )
    args = parser.parse_args()

    knowledge_graph_path = resolve_knowledge_graph_path(args.knowledge_graph)
    bootstrap_assets(knowledge_graph_path)

    server = ThreadingHTTPServer((args.host, args.port), PocketOpsDemoHandler)
    server.sync_version = args.sync_version
    server.min_supported_app_version = args.min_supported_app_version
    server.demo_username = args.demo_username
    server.demo_password = args.demo_password
    print("PocketOps demo mock server is running.")
    print(f"Knowledge graph source: {knowledge_graph_path}")
    print(f"Manifest syncVersion: {args.sync_version}")
    print(f"Manifest minSupportedAppVersion: {args.min_supported_app_version}")
    print(f"Demo login username: {args.demo_username}")
    print(f"Demo login password: {args.demo_password}")
    print("Use one of these URLs in the Android demo:")
    for url in dict.fromkeys(iter_candidate_urls(args.host, args.port)):
        print(f"  {url}")
    print("If you use adb reverse, configure the phone with: http://127.0.0.1:8080")
    print("Press Ctrl+C to stop.")

    try:
        server.serve_forever()
    except KeyboardInterrupt:
        print("\nStopping server...")
    finally:
        server.server_close()


if __name__ == "__main__":
    main()
