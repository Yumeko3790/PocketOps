def iter_knowledge_graph_candidates() -> Sequence[Path]:
    candidates: list[Path] = [
        ROOT / KNOWLEDGE_GRAPH_RELATIVE_PATH,
        ROOT.parent / KNOWLEDGE_GRAPH_RELATIVE_PATH,
    ]

    # Probe sibling project folders without assuming a specific release-style
    # Android repo directory name.
    workspace_root = ROOT.parent
    try:
        siblings = sorted(path for path in workspace_root.iterdir() if path.is_dir())
    except OSError:
        siblings = []

    for sibling in siblings:
        candidates.append(sibling / KNOWLEDGE_GRAPH_RELATIVE_PATH)
        try:
            nested_children = sorted(path for path in sibling.iterdir() if path.is_dir())
        except OSError:
            nested_children = []
        for nested in nested_children:
            candidates.append(nested / KNOWLEDGE_GRAPH_RELATIVE_PATH)

    unique_candidates: list[Path] = []
    seen: set[Path] = set()
    for candidate in candidates:
        if candidate not in seen:
            unique_candidates.append(candidate)
            seen.add(candidate)
    return tuple(unique_candidates)


def resolve_knowledge_graph_path(explicit_path: Path | None = None) -> Path:
    if explicit_path is not None:
        path = explicit_path.expanduser().resolve()
        if not path.exists():
            raise FileNotFoundError(f"knowledge graph not found: {path}")
        return path

    for candidate in iter_knowledge_graph_candidates():
        if candidate.exists():
            return candidate

    tried = "\n".join(f"  - {candidate}" for candidate in iter_knowledge_graph_candidates())
    raise FileNotFoundError(
        "Could not locate maintenance knowledge graph. Tried:\n"
        f"{tried}\n"
        "Pass --knowledge-graph <path> to override."
    )


def load_knowledge_graph_bytes(knowledge_graph_path: Path) -> bytes:
    if not knowledge_graph_path.exists():
        raise FileNotFoundError(f"knowledge graph not found: {knowledge_graph_path}")
    return knowledge_graph_path.read_bytes()


